/**
 * Определяет географическую категорию маршрута
 */
function getRouteCategory(fromSlug: string, toSlug: string): string {
  const southernSlugs = ["sochi", "krasnodar", "simferopol", "yalta", "rostov"];
  const kmvSlugs = ["mineralnye-vody"];
  const uralSlugs = ["ekaterinburg", "chelyabinsk", "tyumen", "perm"];
  const moskowSlugs = ["moskva", "spb", "yaroslavl", "nizhniy-novgorod", "voronezh"];
  const volgaSlugs = ["kazan", "samara", "volgograd"];
  const siberiaSlugs = ["novosibirsk"];

  if (kmvSlugs.includes(fromSlug) || kmvSlugs.includes(toSlug)) return "kmv";
  if (siberiaSlugs.includes(fromSlug) || siberiaSlugs.includes(toSlug)) return "siberia";
  if (uralSlugs.includes(fromSlug) && uralSlugs.includes(toSlug)) return "ural";
  if (southernSlugs.includes(fromSlug) && southernSlugs.includes(toSlug)) return "southern";
  if (southernSlugs.includes(fromSlug) || southernSlugs.includes(toSlug)) return "southern";
  if (moskowSlugs.includes(fromSlug) || moskowSlugs.includes(toSlug)) return "moscow";
  if (volgaSlugs.includes(fromSlug) || volgaSlugs.includes(toSlug)) return "volga";
  if (uralSlugs.includes(fromSlug) || uralSlugs.includes(toSlug)) return "ural";

  return "default";
}

/**
 * Возвращает уникальный контент для маршрута.
 * Если для маршрута нет индивидуальных данных, генерирует по категории.
 */
export function getRouteContent(slug: string, fromSlug: string, toSlug: string): RouteContent {
  // Сначала ищем индивидуальный контент
  if (routeContentData[slug]) {
    return routeContentData[slug];
  }

  // Генерируем по категории
  const category = getRouteCategory(fromSlug, toSlug);
  return getCategoryFallback(category, fromSlug, toSlug);
}

