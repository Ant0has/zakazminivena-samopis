// Длинный контент для хаба аэропорта /airport/{iata}/ (10к+ символов).

import { durationPhrase, formatRubFromInt } from "./format";
import { calcPrice, formatPrice } from "@/lib/routes-data";

export interface AirportHubSection {
  title: string;
  iconKey: "clock" | "car" | "sparkles" | "backpack" | "shield" | "briefcase" | "compass";
  body: string;
}

export interface AirportHubContent {
  heroSubtitleLong: string;
  intro: string;
  sections: AirportHubSection[];
  faq: Array<{ q: string; a: string }>;
}

interface Input {
  iata: string;
  airportName: string;
  airportNameFull: string;
  city: string;
  region: string;
  kmToCenter: number;
  terminals: Array<{ code: string; description: string }>;
  routesCount: number;
  routesMinPrice: number;
}

export function generateAirportHubContent(opts: Input): AirportHubContent {
  const {
    iata,
    airportName,
    airportNameFull,
    city,
    region,
    kmToCenter,
    terminals,
    routesCount,
    routesMinPrice,
  } = opts;

  const iataUpper = iata.toUpperCase();
  const priceFrom = formatPrice(routesMinPrice);

  const heroSubtitleLong =
    `Заказать минивэн в ${airportNameFull} для встречи рейса или отправки в аэропорт. ` +
    `Подаём 6–8 пассажирских мест с багажом ко всем терминалам, цена от ${priceFrom} ₽ за машину, ` +
    `без зависимости от пробок. Расстояние до центра ${city} — около ${kmToCenter} км.`;

  const intro =
    `Аэропорт ${airportNameFull} — один из основных воздушных хабов ${region}. ` +
    `Расположен в ${kmToCenter} км от центра города ${city}, обслуживает прибытия и вылеты ` +
    `как на регулярных, так и на чартерных направлениях. Для пассажиров, прилетающих ` +
    `группой 4–8 человек, минивэн с водителем — самый рациональный способ организовать ` +
    `трансфер: одна машина вместо двух такси, понятная фиксированная цена и встреча с ` +
    `табличкой по фамилии прямо в зоне прилёта.\n\n` +
    `Заказать минивэн в ${airportName} (${iataUpper}) можно онлайн через форму на этой ` +
    `странице или сразу написать менеджеру в WhatsApp/Telegram. После подтверждения ` +
    `заявки за 30 минут до прибытия рейса вам присылают контакт водителя, номер машины ` +
    `и фотографию минивэна. Бесплатное ожидание 60 минут при задержке рейса уже включено ` +
    `в стоимость — никакого нервного «успеют ли», даже если самолёт задерживается.\n\n` +
    `На странице — список всех маршрутов из ${airportName}, по которым мы регулярно ` +
    `подаём минивэн (всего ${routesCount} направлений), с актуальными ценами и временем ` +
    `в пути. Если вашего адреса нет в списке, цена считается по формуле «4 000 ₽ + ` +
    `расстояние × тариф ₽/км» — менеджер пришлёт точную сумму в течение 5 минут.`;

  const sections: AirportHubSection[] = [];

  // 1. Терминалы аэропорта
  sections.push({
    title: `Терминалы аэропорта ${airportName}`,
    iconKey: "compass",
    body:
      `${airportName} имеет ${terminals.length === 1 ? "один пассажирский терминал" : terminals.length + " терминалов"}. ` +
      `Минивэн подаётся к любому из них без дополнительной платы — водитель уточняет ` +
      `точное место встречи в момент подачи (обычно зона прилёта внутренних или ` +
      `международных рейсов). ` +
      terminals
        .map((t) => `Терминал ${t.code}: ${t.description}.`)
        .join(" ") +
      ` Если вы прилетаете с пересадкой и точного терминала пока не знаете — укажите ` +
      `номер рейса при заказе, мы автоматически отследим прибытие через табло аэропорта.`,
  });

  // 2. Как работает встреча
  sections.push({
    title: `Как водитель встречает в ${airportName}`,
    iconKey: "shield",
    body:
      `За 30 минут до фактического прибытия рейса вы получите в WhatsApp или Telegram ` +
      `следующую информацию: имя водителя, государственный номер минивэна, его модель ` +
      `и цвет, фотографию машины снаружи, точное место встречи внутри терминала. ` +
      `Водитель ожидает в зоне выхода после паспортного контроля или у выдачи багажа ` +
      `с табличкой, на которой написана ваша фамилия. Если вы прилетаете с маленькими ` +
      `детьми, коляской или большим количеством чемоданов — водитель помогает вынести ` +
      `вещи до автомобиля. ` +
      `\n\n` +
      `Парковка возле терминалов ${airportName} оплачивается перевозчиком и уже ` +
      `включена в фиксированную цену минивэна. Если ваш рейс задерживается — ` +
      `бесплатное ожидание 60 минут начинается с момента фактического приземления, ` +
      `а не от расписания. Это значит, что даже многочасовая задержка рейса не ` +
      `превратится в неожиданную доплату за стоянку.`,
  });

  // 3. Парк
  sections.push({
    title: `Какие минивэны подаём в ${airportName}`,
    iconKey: "car",
    body:
      `В ${airportName} мы регулярно подаём четыре класса минивэна. Универсальный класс ` +
      `(VW Caravelle, Hyundai H-1) — самый бюджетный вариант для 5–6 пассажиров с ` +
      `обычным багажом. Комфорт-класс (VW Multivan, Hyundai Staria) — наиболее ` +
      `популярный, на 7–8 пассажиров с раздвижными дверями с обеих сторон и большим ` +
      `багажником. Бизнес-класс (Mercedes-Benz V-Class, Toyota Alphard) — для VIP-` +
      `делегаций, с кожаным салоном, индивидуальными креслами с подогревом и ` +
      `климат-контролем по зонам. Большой класс (Mercedes Vito, Toyota Hiace) — для ` +
      `групп 8 человек с туристическим, спортивным или съёмочным инвентарём.\n\n` +
      `Все минивэны парка 2019–2023 года выпуска, проходят техническое обслуживание ` +
      `каждые 10 000 км, имеют исправный климат-контроль, USB-розетки на каждом ряду ` +
      `и тонировку задних стёкол. В каждом минивэне предусмотрены крепления Isofix ` +
      `для детских кресел любого типа.`,
  });

  // 4. Что входит в цену
  sections.push({
    title: "Что входит в цену трансфера",
    iconKey: "sparkles",
    body:
      `В фиксированную стоимость минивэна из ${airportName} (от ${priceFrom} ₽ за машину) ` +
      `включено всё, что нужно для комфортной поездки: подача к выбранному терминалу, ` +
      `парковка возле аэропорта на время ожидания, бензин по маршруту, проезд по ` +
      `платным дорогам в пределах маршрута, табличка с фамилией для встречи, бесплатное ` +
      `ожидание 60 минут при задержке рейса, детское кресло любого типа (бустер, ` +
      `9–18 кг или 18–36 кг), помощь с погрузкой багажа, электронный кассовый чек ` +
      `по 54-ФЗ на email или SMS.\n\n` +
      `Что оплачивается отдельно: дополнительный адрес посадки или высадки — 500 ₽ ` +
      `за каждый, доп.остановка более 15 минут — 500 ₽ или почасовой тариф, ночной ` +
      `тариф 00:00–06:00 — +20% к цене, перевозка собаки 10–25 кг — 1 500 ₽. Никаких ` +
      `скрытых доплат за пробки, погоду или количество пассажиров не бывает — цена ` +
      `за машину фиксируется в момент подтверждения заказа.`,
  });

  // 5. Когда лучше заказывать
  sections.push({
    title: "Когда лучше заказывать минивэн",
    iconKey: "clock",
    body:
      `Для гарантированной подачи минивэна в ${airportName} мы рекомендуем бронировать ` +
      `за 4–24 часа до прибытия рейса. В пиковые часы (вечер пятницы, утро ` +
      `воскресенья, выходные с пятницы по воскресенье) и в высокий сезон ` +
      `(декабрь-январь, июнь-август) лучше резервировать за 24+ часа — это позволит ` +
      `подобрать оптимальный класс минивэна и водителя именно под ваш маршрут. Срочный ` +
      `заказ за 1–3 часа возможен, но количество доступных минивэнов в это время ` +
      `сокращается, а класс может быть только тот, который сейчас свободен.\n\n` +
      `Если вы летаете регулярно из ${city} — мы можем оформить корпоративную ` +
      `подписку: фиксированный водитель и минивэн, гарантированная подача в любое ` +
      `время дня, документы для бухгалтерии и постоплата до 14 дней. Для регулярного ` +
      `корпоративного трансфера в ${airportName} это выгоднее любого такси-` +
      `агрегатора по совокупной стоимости и надёжности подачи.`,
  });

  // 6. Документы для юрлиц
  sections.push({
    title: "Документы для юридических лиц",
    iconKey: "briefcase",
    body:
      `Для корпоративных клиентов и тех, кто оплачивает поездку из бюджета компании, ` +
      `мы готовим полный пакет отчётных документов без дополнительной платы. ` +
      `В пакет входит договор оферты (типовой) или индивидуальный договор с ` +
      `вашими условиями, счёт на оплату с реквизитами вашей организации, акт ` +
      `оказания услуг после поездки, ККТ-чек электронный по 54-ФЗ, ` +
      `универсальный передаточный документ (УПД) — по запросу. ` +
      `Работаем как с НДС, так и без НДС — указывайте требование в момент ` +
      `заключения договора.\n\n` +
      `После трёх оплаченных поездок открываем постоплату до 14 дней — это удобный ` +
      `формат для регулярных корпоративных поездок в ${airportName} или из него. ` +
      `Скидка от объёма: 5% от 50 000 ₽ в месяц, 10% от 150 000 ₽ в месяц. По ` +
      `всем вопросам корпоративного обслуживания работает закреплённый менеджер ` +
      `с SLA ответа 30 минут в рабочие часы (понедельник–пятница 9:00–19:00 МСК).`,
  });

  const faq: Array<{ q: string; a: string }> = [
    {
      q: `Сколько стоит минивэн в аэропорт ${airportName}?`,
      a:
        `Цена зависит от точки подачи и конкретного маршрута. Базовая стоимость — от ` +
        `${priceFrom} ₽ за машину 6–8 мест. Для конкретного маршрута укажите адрес в ` +
        `форме — менеджер пришлёт фикс цену за 5 минут. Все актуальные маршруты с ` +
        `ценами есть в таблице выше.`,
    },
    {
      q: `Сколько ждёте, если рейс задерживается?`,
      a:
        `Бесплатно 60 минут от времени фактического прилёта (не от расписания). ` +
        `Свыше — 500 ₽/час. Время отсчитывается с момента приземления, а не с ` +
        `запланированного времени — поэтому многочасовая задержка не превратится в ` +
        `неожиданную доплату.`,
    },
    {
      q: `Как водитель найдёт меня в ${airportName}?`,
      a:
        `Встреча в зоне прилёта с табличкой по вашей фамилии. За 30 минут до ` +
        `прибытия рейса вы получите имя водителя, номер машины, её фото и ` +
        `WhatsApp/Telegram-контакт. Точное место встречи (выход из конкретного ` +
        `терминала) водитель сообщит за 10–15 минут до фактического приземления.`,
    },
    {
      q: `К каким терминалам подаёте?`,
      a:
        `Ко всем терминалам ${airportName}: ${terminals.map((t) => t.code).join(", ")}. ` +
        `Точное место встречи водитель сообщает в момент подачи. Парковка возле ` +
        `терминала уже включена в фикс цену.`,
    },
    {
      q: `Можно ли с детским креслом или питомцем?`,
      a:
        `Детское кресло любого типа (бустер, 9–18 кг, 18–36 кг) — бесплатно, до 4 ` +
        `кресел одновременно (зависит от модели минивэна). Питомец в переноске — ` +
        `бесплатно, собака до 10 кг — 1 000 ₽, до 25 кг — 1 500 ₽. Предупредите ` +
        `менеджера заранее, чтобы подобрать минивэн с подходящим салоном.`,
    },
    {
      q: `Принимаете ли безналичный расчёт?`,
      a:
        `Да. Картой онлайн (Юкасса/Тинькофф), СБП по номеру телефона, наличные ` +
        `водителю, банковский перевод для юрлиц. Документы для отчётности: договор ` +
        `(оферта или индивидуальный), счёт, акт, ККТ-чек, УПД по запросу. ` +
        `Постоплата до 14 дней после 3 оплаченных поездок.`,
    },
    {
      q: "За сколько надо заказывать?",
      a:
        `Минимум 2–3 часа до подачи в обычное время. В пиковые часы (вечер пятницы, ` +
        `утро воскресенья) и в высокий сезон — за 24+ часа. Для гарантированного ` +
        `подбора нужного класса минивэна рекомендуем 24 часа.`,
    },
    {
      q: `Что входит в фиксированную цену?`,
      a:
        `Подача, парковка у терминала, бензин, проезд по платным дорогам в пределах ` +
        `маршрута, детское кресло, табличка с фамилией, ожидание 60 минут при ` +
        `задержке рейса, ККТ-чек электронный. Доплаты: ночь +20%, доп.остановка ` +
        `500 ₽, доп.адрес 500 ₽, собака 10–25 кг 1 500 ₽.`,
    },
  ];

  return { heroSubtitleLong, intro, sections, faq };
}
