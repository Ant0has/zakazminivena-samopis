// Длинный контент для туристического хаба /destination/{region}/ (10к+ символов).

import { formatRubFromInt } from "./format";
import { formatPrice } from "@/lib/routes-data";

export interface DestinationHubSection {
  title: string;
  iconKey: "clock" | "car" | "sparkles" | "backpack" | "shield" | "compass" | "camera";
  body: string;
}

export interface DestinationHubContent {
  heroSubtitleLong: string;
  intro: string;
  sections: DestinationHubSection[];
  faq: Array<{ q: string; a: string }>;
}

interface Input {
  regionSlug: string;
  regionName: string;
  regionNameAcc: string;
  hubCity: string;
  topPointsShort: string;
  heroIntro: string;
  routesCount: number;
  minPrice: number;
  packingList: string[];
  multidayTours: Array<{ name: string; days: number; priceFrom: number; description: string }>;
}

export function generateDestinationHubContent(opts: Input): DestinationHubContent {
  const {
    regionName,
    regionNameAcc,
    hubCity,
    topPointsShort,
    routesCount,
    minPrice,
    packingList,
    multidayTours,
  } = opts;
  const priceFrom = formatPrice(minPrice);

  const heroSubtitleLong =
    `Заказать минивэн с водителем в ${regionNameAcc}: однодневные поездки и многодневные ` +
    `туры из ${hubCity}. Водитель знает регион — рассказывает о достопримечательностях, ` +
    `делает остановки для фото без доплаты. До 8 пассажиров с багажом, фикс цена от ` +
    `${priceFrom} ₽ за машину.`;

  const intro =
    `${regionName} — один из самых популярных туристических регионов, куда едут на ` +
    `минивэне из ${hubCity} и других городов. Ключевые точки: ${topPointsShort}. ` +
    `На нашем сайте — ${routesCount} актуальных маршрутов в ${regionNameAcc}, по которым ` +
    `водители ездят регулярно и знают дороги, особенности, лучшие точки для фото и ` +
    `проверенные кафе. Цена за минивэн фиксируется заранее (от ${priceFrom} ₽), ` +
    `без сюрпризов в итоговой стоимости поездки.\n\n` +
    `Главное отличие нашего сервиса от обычного такси или Яндекс Go — формат ` +
    `путешествия, а не «довезти из точки А в точку Б». Водитель ${regionNameAcc} ` +
    `делает остановки по вашему желанию, останавливается у смотровых площадок и ` +
    `достопримечательностей, не считая каждую минуту по счётчику. Можно поменять ` +
    `маршрут прямо в дороге — заехать в дополнительную точку или провести больше ` +
    `времени в одной локации.\n\n` +
    `Минивэн подаётся в любую точку ${hubCity} в назначенное вами время. В цену ` +
    `включены детское кресло любого типа, помощь с багажом, бензин, проезд по ` +
    `платным дорогам в пределах маршрута и работа водителя на всё время поездки. ` +
    `Для корпоративных клиентов и групповых туров готовим полный пакет документов: ` +
    `договор, счёт, акт, ККТ-чек.`;

  const sections: DestinationHubSection[] = [];

  // 1. Что включает поездка
  sections.push({
    title: "Что включено в туристическую поездку",
    iconKey: "sparkles",
    body:
      `В фиксированную цену поездки в ${regionNameAcc} входит всё необходимое: подача ` +
      `минивэна в любую точку ${hubCity} в выбранное время, бензин по всему маршруту, ` +
      `проезд по платным дорогам, детское кресло любого типа, помощь с погрузкой ` +
      `багажа, остановки для фото до 30 минут на каждой без доплат, 1–2 санитарные ` +
      `остановки в дороге, бесплатное ожидание в локациях по согласованному маршруту, ` +
      `работа водителя на всё время поездки, электронный кассовый чек ККТ.\n\n` +
      `Что оплачивается отдельно: дополнительный адрес посадки или высадки в ${hubCity} — ` +
      `500 ₽, доп.остановка более 30 минут — 500 ₽ или почасовой тариф 1 500 ₽/час, ` +
      `ночной тариф 00:00–06:00 — +20%, гостиница для водителя в многодневной ` +
      `поездке — 1 500–3 000 ₽/сутки по факту, перевозка собаки крупнее 10 кг — ` +
      `1 000–1 500 ₽. Все надбавки прозрачные и обсуждаются до подтверждения заказа.`,
  });

  // 2. Опыт водителя
  sections.push({
    title: `Опыт водителя в ${regionNameAcc}`,
    iconKey: "compass",
    body:
      `Главное отличие нашего минивэна от обычного такси — водитель знает ${regionName} ` +
      `лично. Это значит: видел все основные достопримечательности на маршрутах, может ` +
      `посоветовать неочевидные точки для фото, знает сезонные особенности дороги — ` +
      `где гололёд зимой, где пробки летом, где платный участок быстрее бесплатного, ` +
      `помнит проверенные кафе и заправки по пути, понимает климат и подскажет, что ` +
      `надеть и взять с собой.\n\n` +
      `Водители проходят медицинский осмотр перед каждой сменой, имеют стаж от 5 ` +
      `лет, проверку по базам ГИБДД и судимости. Если вы уже ездили с нами в ` +
      `${regionNameAcc} и хотите конкретного водителя — укажите его имя в заявке, мы ` +
      `постараемся подобрать его именно на вашу поездку. Языковая поддержка: ` +
      `базовый английский по запросу для иностранных гостей.`,
  });

  // 3. Многодневные туры
  if (multidayTours.length > 0) {
    sections.push({
      title: `Многодневные туры в ${regionNameAcc}`,
      iconKey: "clock",
      body:
        `Для тех, кто хочет провести в ${regionNameAcc} несколько дней с одним и тем же ` +
        `водителем-проводником, мы предлагаем готовые многодневные пакеты: ` +
        multidayTours
          .map(
            (t) =>
              `«${t.name}» — ${t.days} ${t.days === 1 ? "день" : "дня"} от ` +
              `${formatPrice(t.priceFrom)} ₽ за минивэн (${t.description})`,
          )
          .join("; ") +
        `. ` +
        `\n\n` +
        `Все многодневные туры включают работу водителя на весь период, бензин по ` +
        `маршруту и возможность гибко менять программу прямо в поездке. Расходы на ` +
        `гостиницу для водителя оплачиваются отдельно по факту (обычно 1 500–3 000 ₽ ` +
        `за сутки) — водитель сам выбирает удобный отель в точке ночёвки. Для ` +
        `корпоративных групп оформляем договор и счёт, для семейных поездок — ` +
        `обычный чек ККТ.`,
    });
  }

  // 4. Парк под регион
  sections.push({
    title: `Какие минивэны подаём в ${regionNameAcc}`,
    iconKey: "car",
    body:
      `На туристические маршруты в ${regionNameAcc} мы подаём четыре класса минивэна. ` +
      `Универсальный VW Caravelle на 5–6 пассажиров — самый бюджетный вариант для ` +
      `пары или небольшой группы. Комфорт-класс VW Multivan или Hyundai Staria на ` +
      `7–8 пассажиров с раздвижными дверями и большим багажником под рюкзаки и ` +
      `туристический инвентарь. Бизнес-класс Mercedes V-Class или Toyota Alphard ` +
      `с кожаным салоном — для VIP-туров и поездок с гостями. Большой класс ` +
      `Mercedes Vito или Toyota Hiace — для группы 8 человек с большим багажом или ` +
      `съёмочной техникой.\n\n` +
      `Все минивэны парка 2019–2023 года, оснащены климат-контролем, USB-розетками ` +
      `на каждом ряду, тонировкой задних стёкол и креплениями Isofix для детских ` +
      `кресел. Выбор конкретного класса — в форме заказа или при разговоре с ` +
      `менеджером.`,
  });

  // 5. Что взять с собой
  sections.push({
    title: `Что взять с собой в поездку`,
    iconKey: "backpack",
    body:
      `Для комфортной поездки в ${regionNameAcc} рекомендуем взять: ` +
      packingList.join(", ").toLowerCase() +
      `. Это базовый список, который подходит для большинства маршрутов. ` +
      `Если едете в горы или высокогорье — добавьте крем от солнца с высоким SPF ` +
      `и солнцезащитные очки. Если едете в северные регионы — обязательно термос ` +
      `и тёплая шапка даже летом.\n\n` +
      `Детское кресло любого типа предоставляем бесплатно — указывайте возраст и ` +
      `вес ребёнка в форме заявки, мы установим подходящее кресло заранее. Если у ` +
      `вас своё кресло — водитель поможет его установить в салон минивэна и ` +
      `проверит ремни перед стартом. Для длинных поездок в дальние точки ` +
      `${regionName} (200+ км в одну сторону) захватите перекус, воду и зарядки ` +
      `для гаджетов — в салоне минивэна USB-розетки на каждом ряду.`,
  });

  // 6. Документы и оплата
  sections.push({
    title: "Документы и оплата для юрлиц",
    iconKey: "shield",
    body:
      `Для корпоративных туристических поездок в ${regionNameAcc} (тимбилдинги, ` +
      `корпоративы, MICE-мероприятия) мы готовим полный пакет отчётных документов: ` +
      `договор оферты или индивидуальный, счёт на оплату с реквизитами вашей ` +
      `организации, акт оказания услуг, ККТ-чек электронный, УПД по запросу. ` +
      `Работаем как с НДС, так и без — указывайте требование при оформлении.\n\n` +
      `Для постоянных клиентов открываем постоплату в течение 5 рабочих дней ` +
      `и предлагаем скидку от объёма: 5% от 50 000 ₽ в месяц, ` +
      `10% от 150 000 ₽. Дополнительные форматы оплаты: онлайн картой (Юкасса, ` +
      `Тинькофф), СБП по номеру телефона, наличные водителю по факту поездки. ` +
      `Если поездка многодневная — оплата стандартно после возвращения, депозит не ` +
      `требуется.`,
  });

  const faq: Array<{ q: string; a: string }> = [
    {
      q: `Сколько стоит минивэн в ${regionNameAcc}?`,
      a:
        `Базовая цена за машину 6–8 мест — от ${priceFrom} ₽. Конкретные маршруты и ` +
        `их стоимости — в карточках на этой странице. Поездка туда-обратно за день ` +
        `обсуждается с менеджером и зависит от времени ожидания. Многодневная поездка с ` +
        `ночёвкой водителя считается от суточного тарифа + расходы на гостиницу водителя.`,
    },
    {
      q: `Что особенного в поездках по ${regionNameAcc}?`,
      a:
        `Водитель знает ${regionName} и его дороги: где остановиться для лучшего фото, ` +
        `какой маршрут оптимальнее в текущий сезон, что посмотреть, где попить кофе. ` +
        `Это формат туристической поездки, а не «довезти из А в Б», как в обычном ` +
        `такси.`,
    },
    {
      q: `Бывают ли многодневные туры в ${regionNameAcc}?`,
      a:
        multidayTours.length > 0
          ? `Да, ${multidayTours.length} готовых пакета: ${multidayTours.map((t) => `«${t.name}» от ${formatPrice(t.priceFrom)} ₽`).join("; ")}. Также делаем индивидуальный маршрут с ночёвкой водителя.`
          : `Да, по запросу — с ночёвкой водителя в любом пункте региона. Гостиница ` +
            `водителя — 1 500–3 000 ₽/сутки по факту, оплачивается отдельно.`,
    },
    {
      q: `Можно ли менять маршрут в дороге?`,
      a:
        `Да. Это главное преимущество индивидуального минивэна перед агрегаторами: ` +
        `гибкий маршрут, дополнительные остановки до 30 минут — бесплатно, дольше ` +
        `— по почасовому тарифу. Большие отклонения от маршрута (например, заехать ` +
        `в город не из плана) пересчитываются по километражу или по согласованию.`,
    },
    {
      q: `Какой минивэн подадите?`,
      a:
        `Под ваш запрос подбираем класс: универсальный 5–6 мест (Caravelle, H-1), ` +
        `комфорт 7–8 мест (Multivan, Staria), бизнес-премиум (V-Class, Alphard), ` +
        `большой 8 мест с инвентарём (Vito, Hiace). Все 4 класса доступны на любом ` +
        `маршруте в ${regionNameAcc}.`,
    },
    {
      q: `Что взять с собой?`,
      a:
        packingList.slice(0, 5).join(", ") + `. Это базовый набор. Дет.кресла, ` +
        `пледы и помощь с багажом включены в цену — приходить нужно только с тем, ` +
        `что вам лично понадобится в поездке.`,
    },
    {
      q: `Бывают ли корпоративные туры?`,
      a:
        `Да. Для корпоративных клиентов — договор, счёт, акт, УПД, ККТ-чек, ` +
        `постоплата в течение 5 рабочих дней (для постоянных клиентов). Скидки от объёма: ` +
        `5% от 50 тыс. в месяц, 10% от 150 тыс.`,
    },
    {
      q: `Принимаете ли безналичный расчёт?`,
      a:
        `Да: картой онлайн, СБП, банковский перевод для юрлиц. Наличные водителю ` +
        `тоже принимаются. Чек ККТ электронный — на email или SMS сразу после оплаты.`,
    },
  ];

  return { heroSubtitleLong, intro, sections, faq };
}
