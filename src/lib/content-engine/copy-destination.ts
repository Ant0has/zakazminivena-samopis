// Генератор уникального контента для туристического маршрута /destination/{region}/{route}/.
// Цель: 4-5к символов уникального текста.

import { durationPhrase, kmPhrase, priceFromKm, priceRoundTripFromKm } from "./format";
import { calcPrice, formatPrice } from "@/lib/routes-data";

export interface DestinationRouteSection {
  title: string;
  iconKey: "clock" | "car" | "sparkles" | "backpack" | "shield" | "compass" | "camera";
  body: string;
}

export interface DestinationRouteContent {
  h1: string;
  heroSubtitle: string;
  intro: string;
  routeDescription: string;
  callout: string;
  sections: DestinationRouteSection[];
  faq: Array<{ q: string; a: string }>;
}

interface Input {
  regionName: string;
  fromCity: string;
  toCity: string;
  km: number;
  hours: string;
  uniqueIntro?: string;
  uniqueRouteDesc?: string;
  specifics?: string;
  seasonalNotes?: string;
  pointsOfInterest?: Array<{ name: string; description: string }>;
}

export function generateDestinationRouteContent(opts: Input): DestinationRouteContent {
  const price = priceFromKm(opts.km);
  const priceRT = priceRoundTripFromKm(opts.km);
  const price2Day = formatPrice(calcPrice(opts.km) + 8000);
  const duration = durationPhrase(opts.hours);
  const isLong = opts.km > 200;

  const h1 = `Минивэн ${opts.fromCity} → ${opts.toCity} — от ${price} ₽ за машину`;

  const heroSubtitle =
    `Туристическая поездка ${kmPhrase(opts.km)} за ${duration}. Минивэн до 8 пассажиров с ` +
    `багажом. Водитель знает регион, остановки для фото бесплатно, гибкий маршрут.`;

  // ─────── INTRO (3 параграфа) ───────
  const baseIntro =
    `Поездка ${opts.fromCity} → ${opts.toCity} на минивэне с водителем — оптимальный формат ` +
    `для семьи, друзей или туристической группы из 4–8 человек. Заказ оформляется за ` +
    `фиксированную цену ${price} ₽ за всю машину, без сюрпризов в итоговой стоимости. ` +
    `Водитель знает дорогу в ${opts.regionName}, расскажет по пути о ключевых ` +
    `достопримечательностях и подскажет, где лучше остановиться для фото, где попить кофе ` +
    `и какие сезонные особенности маршрута стоит учесть. ` +
    `Это принципиально иной формат поездки, чем такси из Яндекс Go: индивидуальный ` +
    `маршрут, остановки без счётчика, рассказы о регионе.`;

  const introB =
    `Маршрут занимает ${duration} в одну сторону при расстоянии ${kmPhrase(opts.km)}. ` +
    `Подача в любую точку ${opts.fromCity} в назначенное вами время. По пути в ${opts.toCity} ` +
    `водитель сделает 1–2 санитарные остановки, при необходимости — заедет в магазин, кафе ` +
    `или к смотровой площадке. Маршрут гибкий: можете попросить дополнительные точки прямо ` +
    `в дороге, каждая стоит 500 ₽, без скрытых наценок. Поездка туда-обратно за день — ` +
    `по согласованию с менеджером, цена зависит от времени ожидания на месте. ` +
    `Многодневная поездка с ночёвкой водителя в ${opts.toCity} — от ${price2Day} ₽ ` +
    `(гостиница водителя оплачивается отдельно по факту).`;

  const introC =
    `В цену поездки уже включено всё необходимое: подача в любую точку города отправления, ` +
    `бензин по всему маршруту до ${opts.toCity}, проезд по платным дорогам в пределах ` +
    `маршрута, детское кресло любого типа, помощь с багажом, остановки для фото без ` +
    `доплат, бесплатное ожидание в локациях по согласованному маршруту. ` +
    `Документы для отчётности (договор, счёт, акт, УПД, ККТ-чек) — по запросу для ` +
    `юридических лиц без дополнительной платы за оформление.`;

  const intro = opts.uniqueIntro
    ? `${opts.uniqueIntro}\n\n${baseIntro}\n\n${introB}\n\n${introC}`
    : `${baseIntro}\n\n${introB}\n\n${introC}`;

  // ─────── ROUTE DESCRIPTION (2 параграфа) ───────
  const baseRouteDesc =
    `Дорога ${opts.fromCity} → ${opts.toCity} в нашем формате — это не «довезти из точки А в ` +
    `точку Б», а полноценная туристическая программа. Водитель ${opts.regionName} лично знает ` +
    `все ключевые остановки, лучшие смотровые площадки, проверенные кафе и санитарные точки. ` +
    `Если в группе есть пассажиры с особыми пожеланиями — едете быстрее или медленнее, ` +
    `делаете больше или меньше остановок, заезжаете в локации, которых нет в маршруте по ` +
    `умолчанию.`;

  const routeDescB =
    `Минивэн подаётся в обозначенное время к указанному адресу в ${opts.fromCity}. Если ` +
    `вы хотите забрать друзей по дороге — добавьте дополнительный адрес посадки за 500 ₽. ` +
    `На длинных участках водитель подберёт ритм с учётом маленьких детей или пассажиров ` +
    `с укачиванием. Климат-контроль в салоне настраивается зонами, USB-розетки на каждом ` +
    `пассажирском ряду, в багажнике место для 6–8 чемоданов или 8–10 рюкзаков туриста.`;

  const routeDescription = opts.uniqueRouteDesc
    ? `${opts.uniqueRouteDesc}\n\n${baseRouteDesc}\n\n${routeDescB}`
    : `${baseRouteDesc}\n\n${routeDescB}`;

  const callout =
    opts.seasonalNotes ||
    `Берите тёплую куртку и удобную обувь — даже летом в ${opts.regionName} погода может ` +
    `поменяться за час. Водитель остановится для нужных пауз — фото, обед, санитарная.`;

  // ─────── ДОПОЛНИТЕЛЬНЫЕ СЕКЦИИ ───────
  const sections: DestinationRouteSection[] = [];

  // Какой минивэн под маршрут
  sections.push({
    title: "Какой минивэн подаём на этот маршрут",
    iconKey: "car",
    body:
      `На туристическую поездку ${opts.fromCity} → ${opts.toCity} мы предлагаем выбор из ` +
      `четырёх классов: универсальный VW Caravelle на 5–6 пассажиров — самый бюджетный, ` +
      `комфорт-класс VW Multivan или Hyundai Staria на 7–8 пассажиров с раздвижными ` +
      `дверями и большим багажником, бизнес-класс Mercedes V-Class или Toyota Alphard ` +
      `с кожаным салоном для VIP-туров, и большой класс Mercedes Vito или Toyota Hiace ` +
      `для группы 8 человек с туристическим инвентарём. Все минивэны 2019–2023 года, ` +
      `с климат-контролем, USB-зарядками и опытными водителями региона.`,
  });

  // Опыт водителя в регионе
  sections.push({
    title: `Опыт водителя в ${opts.regionName}`,
    iconKey: "compass",
    body:
      `Главное отличие нашего минивэна от обычного такси — водитель знает ${opts.regionName}. ` +
      `Это значит: видел все основные достопримечательности на маршруте ${opts.fromCity} → ` +
      `${opts.toCity}, может посоветовать неочевидные точки для фото, знает сезонные ` +
      `особенности дороги (где гололёд зимой, где пробки летом, где платный участок ` +
      `быстрее бесплатного), помнит проверенные кафе и заправки по пути. Водители проходят ` +
      `медосмотр перед каждой сменой, имеют стаж от 5 лет и положительные отзывы клиентов. ` +
      `Если хотите конкретного водителя из прошлой поездки — укажите его имя в заявке.`,
  });

  // POI — что посмотреть
  if (opts.pointsOfInterest && opts.pointsOfInterest.length > 0) {
    const top = opts.pointsOfInterest.slice(0, 4).map((p) => `${p.name} — ${p.description}`);
    sections.push({
      title: `Что посмотреть в ${opts.toCity} по пути`,
      iconKey: "camera",
      body:
        `На маршруте ${opts.fromCity} → ${opts.toCity} рекомендуем заехать в эти точки ` +
        `(остановка до 30 минут — бесплатно, дольше — почасовой тариф или по согласованию): ` +
        `${top.join("; ")}. Если хотите посмотреть что-то ещё — заранее предупредите ` +
        `менеджера, чтобы скорректировать маршрут и время выезда из ${opts.fromCity}.`,
    });
  }

  // Многодневный сценарий
  sections.push({
    title: "Многодневная поездка с ночёвкой водителя",
    iconKey: "clock",
    body:
      `Если вы планируете провести в ${opts.toCity} 1–2 дня и нужен водитель на всё время ` +
      `пребывания — мы предлагаем многодневный сценарий. От ${price2Day} ₽ ` +
      `за минивэн с ночёвкой водителя в ${opts.toCity} (отель для водителя оплачивается ` +
      `отдельно по факту, обычно 1 500–3 000 ₽ за сутки). Водитель остаётся в зоне ` +
      `досягаемости и готов отвезти вас на дополнительные экскурсии или вернуться в ` +
      `${opts.fromCity} в любое удобное время. Для туристических групп это часто выгоднее, ` +
      `чем брать такси на каждый отдельный переезд внутри ${opts.regionName}.`,
  });

  // Что включено
  sections.push({
    title: "Что включено в стоимость и что оплачивается отдельно",
    iconKey: "sparkles",
    body:
      `Включено в фикс цену ${price} ₽: подача в любую точку ${opts.fromCity}, бензин по ` +
      `маршруту до ${opts.toCity} (${kmPhrase(opts.km)}), детское кресло любого типа, ` +
      `помощь с багажом, 1–2 санитарные остановки, остановки для фото до 30 минут на каждой, ` +
      `работа водителя за все ${duration} в пути, электронный ККТ-чек на email. ` +
      `Отдельно оплачивается: дополнительный адрес посадки/высадки 500 ₽, доп.остановка ` +
      `свыше 30 минут — 500 ₽ или почасовой тариф 1 500 ₽/час, ночной тариф 00:00–06:00 — ` +
      `+20%, ночёвка водителя в дальней поездке — от 1 500 ₽/сутки за гостиницу, перевозка ` +
      `собаки крупнее 10 кг — 1 000–1 500 ₽.`,
  });

  // Что взять с собой / специфика
  sections.push({
    title: opts.specifics ? `Особенности поездки в ${opts.toCity}` : "Что взять с собой",
    iconKey: "backpack",
    body: opts.specifics
      ? opts.specifics +
        `\n\nДополнительно: для длинной поездки возьмите воду, лёгкий перекус, зарядку для ` +
        `гаджетов, тёплую кофту (даже летом). Если едут маленькие дети — любимую игрушку и ` +
        `пелёнку. В салоне минивэна USB-розетки и климат-контроль.`
      : `Для поездки в ${opts.regionName} рекомендуем взять: удобную обувь для пеших ` +
        `прогулок на остановках, тёплую куртку или ветровку (погода в регионе может ` +
        `поменяться за час), документы пассажиров (для отдельных точек могут проверять ` +
        `паспорт), наличные деньги на сувениры или мелкие покупки в путевых точках, ` +
        `зарядку для телефона (в салоне минивэна USB-розетки, но при остановках в ` +
        `музеях/локациях лучше иметь свою), фотоаппарат или хороший телефон — будут ` +
        `остановки для фото. Дет.кресло предоставим бесплатно — указывайте тип в заявке.`,
  });

  // ─────── FAQ ───────
  const faq: Array<{ q: string; a: string }> = [
    {
      q: `Сколько стоит минивэн ${opts.fromCity} → ${opts.toCity}?`,
      a:
        `Фикс цена от ${price} ₽ за машину 6–8 мест. Не зависит от количества пассажиров. ` +
        `Поездка туда-обратно за день — по согласованию с менеджером, зависит от времени ` +
        `ожидания на месте. С ночёвкой водителя в ${opts.toCity} — от ${price2Day} ₽ ` +
        `(плюс гостиница для водителя по факту, обычно 1 500–3 000 ₽/сутки).`,
    },
    {
      q: `Сколько по времени поездка ${opts.fromCity} → ${opts.toCity}?`,
      a:
        `${duration} в одну сторону, расстояние ${opts.km} км. Время с остановками для ` +
        `фото и санитарными паузами считаем отдельно — на пути в ${opts.regionName} обычно ` +
        `закладываем 1–2 остановки общей продолжительностью 30–60 минут. Если хотите ` +
        `больше остановок — заранее предупредите менеджера для корректировки графика.`,
    },
    {
      q: "Можно ли менять маршрут в дороге?",
      a:
        `Да, это и есть главное преимущество индивидуального минивэна перед Яндекс Go и ` +
        `обычным такси: можете попросить новые точки, остановки для фото, заезд в кафе — ` +
        `водитель ждёт без доплаты до 30 минут на каждой остановке. Дополнительные адреса ` +
        `с большим отклонением от маршрута — по 500 ₽ за каждый. Большие изменения ` +
        `(например, ехать в другой город вместо ${opts.toCity}) пересчитываются по ` +
        `километражу.`,
    },
    {
      q: opts.specifics
        ? `Что особенного на маршруте ${opts.fromCity} → ${opts.toCity}?`
        : `Что взять с собой в поездку?`,
      a:
        opts.specifics ||
        `Удобную обувь для пеших прогулок на остановках, тёплую куртку (погода в ` +
        `${opts.regionName} может поменяться), документы пассажиров, наличные на ` +
        `сувениры. Дет.кресла предоставляем бесплатно — указывайте тип в заявке (бустер, ` +
        `9–18 кг или 18–36 кг).`,
    },
    {
      q: "Можно ли с детьми?",
      a:
        `Да. Детское кресло любого типа (бустер, 9–18 кг, 18–36 кг) — бесплатно. ` +
        `Минивэн просторный, у детей есть место для игр или сна в дороге. Для ` +
        `${isLong ? "длинной поездки в " + opts.toCity : "поездки в " + opts.toCity} ` +
        `захватите любимую игрушку, бутылочку воды и небольшой перекус. Климат-контроль ` +
        `и USB-зарядки в салоне есть.`,
    },
    {
      q: "Кто будет за рулём?",
      a:
        `Опытный водитель со стажем от 5 лет, знающий ${opts.regionName} лично. Перед ` +
        `каждой сменой медицинский осмотр, проверка по базам ГИБДД и судимости. Знание ` +
        `региона и проверенные маршруты — главное отличие от обычного такси. Если вы уже ` +
        `ездили с нами и хотите конкретного водителя — укажите его имя в заявке, мы ` +
        `постараемся подобрать.`,
    },
    {
      q: "Как оплатить?",
      a:
        `Картой онлайн в момент заказа (Юкасса/Тинькофф), СБП по номеру телефона, ` +
        `наличными водителю или банковским переводом для юрлиц. Документы для ` +
        `отчётности: договор, счёт, акт, УПД, ККТ-чек электронный по 54-ФЗ. Для ` +
        `корпоративных клиентов — постоплата до 14 дней после 3 оплаченных поездок.`,
    },
    {
      q: "Что если придётся отменить заказ?",
      a:
        `За 24 часа до подачи — полный возврат. За 6–24 часа — 50%. Меньше 6 часов — ` +
        `без возврата, потому что водитель уже выехал к точке подачи. В случае ` +
        `нелётной погоды, отмены вашего мероприятия из-за форс-мажора (ЧС, болезнь) — ` +
        `обсуждаем индивидуально, как правило возвращаем 100%. Перенос даты ` +
        `бесплатный за 24+ часа.`,
    },
  ];

  return {
    h1,
    heroSubtitle,
    intro,
    routeDescription,
    callout,
    sections,
    faq,
  };
}
