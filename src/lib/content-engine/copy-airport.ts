// Генератор уникального текстового контента для маршрута аэропорта.
// Используется на /airport/{iata}/{destination}/ — заголовки, абзацы, FAQ.
//
// Цель: 4-5к символов уникального текста на странице (после подстановки переменных).
// Источники СЯ: research_clusters.md + sya_wordstat.csv + sya_topwords.csv.
// Ключевые n-граммы конкурентов: «минивэн такси», «минивэн аэропорт», «mercedes benz»,
// «руб час», «развозка сотрудников», «заказать минивэн», «аренда минивэна».

import {
  BRAND,
  durationPhrase,
  kmPhrase,
  priceFromKm,
  pricePerPersonFromKm,
  priceRoundTripFromKm,
} from "./format";
import { calcPrice } from "@/lib/routes-data";

export interface AirportRouteContent {
  h1: string;
  heroSubtitle: string;
  /** Длинный intro в лонгриде (3-4 параграфа). */
  intro: string;
  /** Описание маршрута и дороги (2 параграфа). */
  routeDescription: string;
  /** Совет/выноска для callout */
  tipForTravel: string;
  longreadCallout: string;
  /** Дополнительные секции лонгрида с уникальными текстами (по 1 параграфу каждая). */
  sections: AirportRouteSection[];
  /** 8 FAQ — с подстановкой данных */
  faq: Array<{ q: string; a: string }>;
}

export interface AirportRouteSection {
  /** Заголовок секции (ALLCAPS на рендере) */
  title: string;
  /** Иконка-категория для UI (clock, car, sparkles, backpack, shield, briefcase) */
  iconKey: "clock" | "car" | "sparkles" | "backpack" | "shield" | "briefcase";
  /** Тело секции */
  body: string;
}

interface Input {
  iata: string;
  airportName: string;
  airportNameFull: string;
  destinationName: string;
  destinationCity: string;
  km: number;
  hours: string;
  uniqueIntro?: string;
  uniqueRouteDesc?: string;
}

export function generateAirportRouteContent(opts: Input): AirportRouteContent {
  const price = priceFromKm(opts.km);
  const priceRT = priceRoundTripFromKm(opts.km);
  const pricePer = pricePerPersonFromKm(opts.km, 7);
  const priceTwoTaxiTax = calcPrice(opts.km) * 2;
  const duration = durationPhrase(opts.hours);
  const iataUpper = opts.iata.toUpperCase();
  const isLong = opts.km > 100;
  const isShort = opts.km < 50;

  const h1 = `Минивэн из ${opts.airportNameFull} в ${opts.destinationName} — от ${price} ₽`;

  const heroSubtitle =
    `Трансфер с водителем на ${opts.km} км за ${duration}. Минивэн 6–8 пассажиров с багажом. ` +
    `Фикс цена за машину, не за пассажира. Встреча в зоне прилёта с табличкой по фамилии.`;

  // ─────── INTRO (3-4 параграфа) ───────
  const baseIntro =
    `Минивэн ${opts.airportName} (${iataUpper}) — ${opts.destinationName} — самый удобный ` +
    `способ перевезти семью или группу коллег от терминала до пункта назначения. ` +
    `Расстояние ${kmPhrase(opts.km)}, время в пути ${duration} в зависимости от пробок и ` +
    `времени суток. Это бюджетный трансфер для тех, кто прилетает группой 4–8 человек: ` +
    `цена ${price} ₽ за весь минивэн — около ${pricePer} ₽ на пассажира при полной загрузке, ` +
    `что значительно выгоднее двух отдельных машин такси (которые на этом маршруте обойдутся ` +
    `в районе ${formatPriceShort(priceTwoTaxiTax)} ₽ суммарно).`;

  const introB =
    `Заказать минивэн в ${opts.airportName} можно онлайн на этой странице или через ` +
    `WhatsApp/Telegram менеджеру. После заявки в течение 5 минут менеджер подтверждает ` +
    `фикс цену и присылает контакт водителя — никакой неопределённости по итогу поездки. ` +
    `За 30 минут до прибытия рейса водитель отслеживает табло, выезжает к терминалу заранее ` +
    `и встречает пассажиров сразу после паспортного контроля с табличкой по фамилии. ` +
    `Бесплатное ожидание 60 минут уже включено в стоимость — если рейс задерживается, ` +
    `доплаты за это не будет.`;

  const introC =
    `В цену поездки на минивэне ${iataUpper} → ${opts.destinationName} включено всё нужное ` +
    `для комфортной семейной поездки: бензин, проезд по платным дорогам в пределах маршрута, ` +
    `детское кресло любого типа (бустер, 9–18 кг или 18–36 кг), помощь с багажом до подъезда, ` +
    `табличка с фамилией и кассовый чек ККТ на электронную почту. Для юридических лиц ` +
    `подготавливаем договор, счёт на оплату, акт оказания услуг и УПД — корпоративный ` +
    `минивэн с документами для бухгалтерии без дополнительной платы за оформление.`;

  const intro = opts.uniqueIntro
    ? `${opts.uniqueIntro}\n\n${baseIntro}\n\n${introB}\n\n${introC}`
    : `${baseIntro}\n\n${introB}\n\n${introC}`;

  // ─────── ROUTE DESCRIPTION (2 параграфа) ───────
  const baseRouteDesc =
    `Водитель выезжает к терминалу заранее с запасом на возможные пробки. Информация о ` +
    `рейсе обновляется онлайн через табло аэропорта, поэтому контакт ` +
    `с пассажирами устанавливается ещё до того, как они заберут багаж. По маршруту ` +
    `${opts.airportName} — ${opts.destinationName} (${kmPhrase(opts.km)}) водитель выбирает ` +
    `оптимальный путь, основываясь на текущей дорожной ситуации. При желании можно сделать ` +
    `одну короткую остановку без доплаты — у супермаркета, аптеки или АЗС.`;

  const routeDescB =
    `Туда-обратно за день — стандартный сценарий для гостей города: цена ${priceRT} ₽ за обе ` +
    `поездки (скидка 20% на обратный путь). Машина ожидает вас в указанном месте до ` +
    `завершения дел — без счётчика и стресса. Удобно для встреч с родственниками, ` +
    `корпоративных переговоров, экскурсионной программы по городу. Если планируется ` +
    `пересадка на следующий рейс из ${opts.airportName} в другой аэропорт — мы знаем ` +
    `все логистические нюансы Москвы и доставим вовремя.`;

  const routeDescription = opts.uniqueRouteDesc
    ? `${opts.uniqueRouteDesc}\n\n${baseRouteDesc}\n\n${routeDescB}`
    : `${baseRouteDesc}\n\n${routeDescB}`;

  const tipForTravel =
    `Бронируйте минивэн заранее — за 2–24 часа до подачи, особенно в пиковые часы прилёта ` +
    `(вечер пятницы, утро воскресенья). Контакт водителя приходит за 30 минут до прибытия ` +
    `рейса, поэтому ожидания в зоне прилёта обычно не бывает.`;

  const longreadCallout =
    `Бесплатное ожидание 60 минут при задержке рейса — уже включено в фикс цену. ` +
    `Дольше — по часовому тарифу 500 ₽/час, что в 2–3 раза дешевле обычного такси.`;

  // ─────── ДОПОЛНИТЕЛЬНЫЕ СЕКЦИИ ───────
  const sections: AirportRouteSection[] = [];

  // 1. Парк минивэнов под маршрут
  sections.push({
    title: "Какой минивэн подаём на этот маршрут",
    iconKey: "car",
    body:
      `Под трансфер ${iataUpper} — ${opts.destinationName} мы подаём один из четырёх классов: ` +
      `универсальный (VW Caravelle, Hyundai H-1) — самый бюджетный вариант на 6 пассажиров; ` +
      `комфорт (VW Multivan, Hyundai Staria) — с раздвижными дверями и просторным салоном ` +
      `на 7–8 пассажиров; бизнес (Mercedes-Benz V-Class, Toyota Alphard) — премиум-минивэн с ` +
      `кожаным салоном и индивидуальными креслами; большой (Mercedes Vito, Toyota Hiace) — ` +
      `для группы 8 человек с большим багажом, спортивным инвентарём или съёмочной техникой. ` +
      `Класс минивэна выбирайте в форме заказа — все 4 варианта подаются в ${opts.airportName} ` +
      `по фикс цене за машину.`,
  });

  // 2. Особенности подачи в этот аэропорт
  sections.push({
    title: `Как водитель встречает в ${opts.airportName}`,
    iconKey: "shield",
    body:
      `За 30 минут до фактического прибытия рейса вы получите в WhatsApp или Telegram имя ` +
      `водителя, государственный номер минивэна, фотографию машины и точное место встречи. ` +
      `Водитель ожидает в зоне выдачи багажа или на выходе из зоны прилёта с табличкой, ` +
      `на которой написана ваша фамилия. Если вы прилетаете с детьми, помогает с коляской ` +
      `и сумками до автомобиля. Парковка у терминала ${opts.airportName} оплачивается ` +
      `перевозчиком — для вас никаких доплат и наличного расчёта на месте. Если рейс ` +
      `сильно задерживается (более 60 минут от расписания), бесплатное ожидание начинается ` +
      `с фактического приземления — мы не списываем минуты, пока вы ждёте свой багаж.`,
  });

  // 3. Что включено в цену
  sections.push({
    title: "Что включено в цену и что оплачивается отдельно",
    iconKey: "sparkles",
    body:
      `В фиксированную стоимость ${price} ₽ за машину входит: подача минивэна к выбранному ` +
      `терминалу, ожидание 60 минут при задержке рейса, бензин по всему маршруту до ` +
      `${opts.destinationName} (${kmPhrase(opts.km)}), детское кресло любого типа, табличка с ` +
      `фамилией, помощь с погрузкой багажа, электронный кассовый чек по 54-ФЗ. ` +
      `Дополнительные опции при необходимости: встреча у вагона на ж/д вокзале — 800 ₽, ` +
      `дополнительный адрес посадки или высадки — 500 ₽, ночной тариф 00:00–06:00 — наценка ` +
      `20%, перевозка собаки 10–25 кг — 1 500 ₽. Никаких скрытых доплат за пробки, погоду ` +
      `или количество пассажиров — цена за машину фиксируется в момент подтверждения заказа.`,
  });

  // 4. Кому подходит этот маршрут
  sections.push({
    title: "Кому подходит трансфер на минивэне",
    iconKey: "briefcase",
    body:
      `Семьи с детьми — детское кресло любого типа бесплатно, в салоне просторно для коляски ` +
      `и большого количества чемоданов. Бизнес-делегации и группы коллег — заключаем договор ` +
      `с компанией, выставляем счёт, акт, ККТ-чек, для постоянных клиентов открываем ` +
      `постоплату до 14 дней. Туристические группы 6–8 человек — минивэн обходится дешевле ` +
      `двух такси и удобнее, чем общественный транспорт с пересадками. Корпоративные клиенты ` +
      `— регулярная развозка сотрудников из ${opts.airportName} по адресам с фиксированным ` +
      `маршрутом и помесячным отчётом. Гости, прилетающие на свадьбу или мероприятие — ` +
      `комфортный групповой трансфер вместо набора такси и автобусов.`,
  });

  // 5. Чем минивэн отличается от обычного такси / Яндекс Go
  sections.push({
    title: "Минивэн против Яндекс Go и обычного такси",
    iconKey: "sparkles",
    body:
      `В Яндекс Go или Uber минивэн как класс часто недоступен или подаётся непредсказуемо: ` +
      `вам могут прислать «комфорт+» с водителем без места для багажа. Наш минивэн всегда ` +
      `точно соответствует заявленному классу — VW Multivan, Hyundai Staria, Mercedes V-Class ` +
      `или Toyota Alphard. Главное отличие — фиксированная цена за машину: вы знаете ` +
      `стоимость поездки ${opts.airportName} → ${opts.destinationName} ещё до того, как ` +
      `сесть в минивэн (${price} ₽), и она не меняется ни от пробок, ни от длины маршрута. ` +
      `Второе отличие — документы для отчётности: договор, счёт, акт, УПД, ККТ-чек. ` +
      `Яндекс Go не оформляет такие документы, поэтому корпоративные клиенты теряют ` +
      `возможность списать поездку на расходы компании.`,
  });

  // 6. Полезное в дорогу (особенности по km)
  sections.push({
    title: isLong
      ? "Длинный маршрут: что взять с собой"
      : isShort
      ? "Короткий трансфер: о чём предупредить водителя"
      : "Полезные мелочи для комфортной поездки",
    iconKey: "backpack",
    body: isLong
      ? `Маршрут ${opts.airportName} → ${opts.destinationName} занимает ${duration} и ` +
        `${kmPhrase(opts.km)} в одну сторону — это длинная поездка, к которой стоит ` +
        `подготовиться. Заранее зарядите телефоны и наушники, возьмите небольшой плед, ` +
        `воду и снек на пассажира. В салоне минивэна есть USB-зарядки и климат-контроль. ` +
        `Если едут маленькие дети — захватите любимую игрушку и пелёнку. По дороге водитель ` +
        `сделает 1–2 санитарных остановки. На длинных маршрутах (более 500 км) подключаем ` +
        `второго водителя для пересменки или ночуем в точке назначения.`
      : isShort
      ? `Короткий трансфер ${opts.airportName} → ${opts.destinationName} — всего ${duration} ` +
        `и ${kmPhrase(opts.km)}. Предупредите менеджера, если едете с маленьким ребёнком ` +
        `(подберём кресло заранее), если у вас крупный или хрупкий багаж (приготовим ремни ` +
        `и пледы для защиты), если нужен бесплатный Wi-Fi в дороге для срочной работы. ` +
        `Кофе или вода в дорогу — по запросу при бронировании.`
      : `Поездка занимает ${duration} — для большинства пассажиров это комфортная ` +
        `продолжительность без необходимости специально готовиться. Возьмите телефон и зарядку ` +
        `(в салоне USB-розетки), документы пассажиров на случай, если ребёнок без сопровождения ` +
        `родителя. Если в группе кто-то склонен к укачиванию — попросите водителя ехать в ` +
        `плавном режиме. На пути возможна одна короткая остановка по запросу без доплаты.`,
  });

  // ─────── FAQ (8 вопросов с подстановкой) ───────
  const faq: Array<{ q: string; a: string }> = [
    {
      q: `Сколько стоит минивэн из ${opts.airportName} в ${opts.destinationName}?`,
      a:
        `Фиксированная цена — от ${price} ₽ за машину 6–8 пассажиров. Не зависит от ` +
        `количества пассажиров, пробок и времени в пути. Туда-обратно за день со скидкой ` +
        `20% на обратный путь — ${priceRT} ₽. Если планируется ночёвка водителя в ` +
        `${opts.destinationName}, добавляется суточная ставка от 8 000 ₽ за машину.`,
    },
    {
      q: `Сколько по времени едет минивэн ${opts.airportName} → ${opts.destinationName}?`,
      a:
        `${duration} в зависимости от пробок и времени суток. Расстояние ${opts.km} км. ` +
        `Водитель отслеживает дорожную ситуацию в реальном времени и выбирает оптимальный ` +
        `маршрут. В часы пик (утро будних дней 7–10 ч и вечер 17–20 ч) поездка может ` +
        `занять верхнюю границу диапазона, ночью — нижнюю.`,
    },
    {
      q: "Что если самолёт задерживается?",
      a:
        `Бесплатное ожидание 60 минут от факта приземления — это уже включено в стоимость ` +
        `${price} ₽. Если задержка дольше, тариф 500 ₽/час, что в 2–3 раза дешевле обычного ` +
        `такси. Главное — мы отсчитываем время от реального приземления, а не от расписания, ` +
        `поэтому многочасовые задержки рейса не превратятся в дополнительные платежи.`,
    },
    {
      q: `Как водитель найдёт меня в ${opts.airportName}?`,
      a:
        `За 30 минут до прибытия рейса вы получите в WhatsApp/Telegram имя водителя, ` +
        `номер минивэна, его фотографию и точное место встречи. Водитель встречает в зоне ` +
        `прилёта с табличкой, на которой написана ваша фамилия. Если у вас не работает ` +
        `интернет в роуминге — дайте контакт встречающего, и менеджер свяжется с ним.`,
    },
    {
      q: "Можно ли с детским креслом?",
      a:
        `Да, бесплатно. Указывайте тип в форме: бустер (от 6 лет), детское кресло 9–18 кг ` +
        `(до 4 лет) или 18–36 кг (4–12 лет). До 4 кресел одновременно — зависит от модели ` +
        `минивэна. Если у вас своё кресло — водитель поможет правильно установить его в ` +
        `салоне и проверит ремни перед стартом.`,
    },
    {
      q: "Принимаете ли безналичный расчёт и документы для юрлиц?",
      a:
        `Да. Картой онлайн (Юкасса/Тинькофф), СБП по номеру телефона, наличными водителю ` +
        `или банковским переводом. Документы для отчётности: договор оферты или ` +
        `индивидуальный договор, счёт на оплату, акт оказания услуг, ККТ-чек электронный ` +
        `по 54-ФЗ, УПД по запросу. Для постоянных корпоративных клиентов — постоплата ` +
        `до 14 дней после 3 оплаченных поездок.`,
    },
    {
      q: "Можно ли заехать по пути в другой адрес?",
      a:
        `Одна короткая остановка (АЗС, аптека, магазин на 5–10 минут) — бесплатно. ` +
        `Дополнительный адрес посадки или высадки — 500 ₽. Если маршрут существенно ` +
        `отклоняется от прямого пути в ${opts.destinationName}, тариф пересчитывается ` +
        `по километражу или почасовая аренда (от 1 500 ₽/час в Москве).`,
    },
    {
      q: "Можно ли с домашним питомцем?",
      a:
        `Да. Питомец в переноске — бесплатно. Собака до 10 кг (без переноски) — 1 000 ₽, ` +
        `10–25 кг — 1 500 ₽, свыше 25 кг — по согласованию (нужен большой минивэн вроде ` +
        `Toyota Hiace или Mercedes Vito). Заранее предупредите менеджера, чтобы подобрать ` +
        `минивэн с подходящим салоном и взять чехлы для защиты обивки.`,
    },
  ];

  return {
    h1,
    heroSubtitle,
    intro,
    routeDescription,
    tipForTravel,
    longreadCallout,
    sections,
    faq,
  };
}

export function airportRouteBadges(_iata: string): string[] {
  return ["Фикс цена", "Встреча с табличкой", "60 мин ожидания", "Дет.кресла"];
}

export function airportRouteCardSubtitle(opts: { km: number; hours: string }): string {
  return `${opts.km} км · ${durationPhrase(opts.hours)} · от ${priceFromKm(opts.km)} ₽`;
}

